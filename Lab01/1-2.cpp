#include <iostream>
using namespace std;

//02 sizeof() : 변수의 byte크기를 return
int main(){
	unsigned short siX;
	unsigned iX;
	long liX;
	long long lliX;

	cout << "sizeof(siX):" << sizeof(siX) << endl;
	cout << "sizeof(iX):" << sizeof(iX) << endl;
	cout << "sizeof(liX):" << sizeof(liX) << endl;
	cout << "sizeof(lliX):" << sizeof(lliX) << endl;
}
